def _seed_relationships(db):
    provisions = {p.citation: p.id for p in db.query(Provision).all()}
    for src_cite, tgt_cite, rel_type, desc in RELATIONSHIPS:
        src_id = provisions.get(src_cite)
        tgt_id = provisions.get(tgt_cite)
        if src_id and tgt_id:
            db.add(ProvisionRelationship(
                source_id=src_id, target_id=tgt_id,
                relationship_type=rel_type, description=desc, confidence=1.0))
    db.flush()
