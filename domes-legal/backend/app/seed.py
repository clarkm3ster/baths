def _j(obj):
    return json.dumps(obj)


def seed():
    Base.metadata.create_all(engine)
    db = SessionLocal()
    if db.query(Provision).count() > 0:
        db.close()
        return
    _seed_provisions(db)
    _seed_taxonomy_tags(db)
    _seed_relationships(db)
    db.commit()
    db.close()


def _seed_provisions(db):
    provisions = _get_provisions()
    for p in provisions:
        parsed = parse_citation(p["citation"])
        aw = json.loads(p.get("applies_when", "{}")) if isinstance(p.get("applies_when"), str) else p.get("applies_when", {})
        tags = [p["domain"], p["provision_type"]]
        for k in aw:
            tags.append(k)
        populations = []
        for k, v in aw.items():
            if isinstance(v, list):
                populations.extend(v)
        prov = Provision(
            citation=p["citation"],
            title=p["title"],
            full_text=p["full_text"],
            source_type=parsed["source_type"],
            title_number=parsed.get("title_number"),
            chapter=parsed.get("chapter"),
            part=parsed.get("part"),
            section=parsed.get("section"),
            subsection=parsed.get("subsection"),
            domain=p["domain"],
            provision_type=p["provision_type"],
            applies_when=p.get("applies_when", "{}"),
            enforcement_mechanisms=p.get("enforcement_mechanisms", "[]"),
            cross_references=p.get("cross_references", "[]"),
            source_url=p.get("source_url", ""),
            tags=json.dumps(tags),
            populations=json.dumps(list(set(populations))),
            confidence_score=1.0,
        )
        db.add(prov)
    db.flush()


